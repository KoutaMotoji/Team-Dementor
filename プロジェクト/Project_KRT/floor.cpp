#include "floor.h"
#include "manager.h"


CFloor::CFloor()
{

}

CFloor::~CFloor()
{

}

//==========================================================================================
//初期化処理
//==========================================================================================
void CFloor::Init()
{
	//テクスチャの登録・割り当て

	int nIdx = CManager::GetInstance()->GetTexture()->Regist("data\\TEXTURE\\GroundBT.jpg");
	BindTexture(CManager::GetInstance()->GetTexture()->GetAddress(nIdx));

	CObject::SetType(TYPE_3D_FIELD);
	CObject3D::Init();
}

//==========================================================================================
//終了処理
//==========================================================================================
void CFloor::Uninit()
{
	CObject3D::Uninit();
}

//==========================================================================================
//更新処理
//==========================================================================================
void CFloor::Update()
{
	CObject3D::Update();
}

//==========================================================================================
//描画処理
//==========================================================================================
void CFloor::Draw()
{
	CObject3D::Draw();
}


//==========================================================================================
//生成処理
//==========================================================================================
CFloor* CFloor::Create(D3DXVECTOR3 pos)
{
	CFloor* field = new CFloor;
	field->SetPolygonParam(pos, FLOOR_SIZE, FLOOR_SIZE);
	field->Init();
	field->SetRot({ 0.0f,0.0f,0.0f });
	return field;
}
